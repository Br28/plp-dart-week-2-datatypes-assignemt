void main(List<String> args) {
  // Datatypes assignment

  // int
  int number1 = 1000;
  int number2 = 500;
  int res = number1 * number2;
  print(res);

  // double
  double savings = 1520;
  double durationInMonths = 5;
  double totalSavings = savings * durationInMonths;
  print(totalSavings);

  // String
  String phrase =
      'Always practice programming, in order for you to be a great programmer';
  print(phrase);

  // List
  List phoneBrands = ['Google Pixel', 'Huawei', 'Samsungs', 'Apple', 'Tecno'];
  print(phoneBrands);

  // Map
  Map tvChannels = {
    'Zambezi Magic': 162,
    'Supersport 3': 223,
    'WWE': 128,
    'TMC': 137
  };
  print(tvChannels);
}
